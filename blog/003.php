            <li><p>Help your dog get more exercise! Playing with your dog strengthens your bond and creates more trust between the two of you. A trusting dog is a listening dog. Exercise also helps reduce energy and aids in the production of stress-reducing hormones that will help keep your pet calm.</p></li>
          </ul> <!-- /section 3: anxiety & fear -->


          <!-- section 4: watchdog -->
          <h2 class="font-weight-bold pb-4">The Watchdog</h2>
          <p>Your dog might bark at every moving branch, just to let you know it’s moving. Or she might bark out of excitement when someone comes up to the door. Either way, your dog believes that it is fulfilling its duty by keeping you safe and up to date on your surroundings. </p>

          <ul class="py-4">
            <li><p>In this case, the most efficient thing to do is…are you ready for this? <em>Train your dog to bark!</em> This is something you will want to start working on as soon and as consistently as you can. By teaching your dog a “speaking” command you can subsequently teach your dog a “quiet” command. This gives the control back to you! Once your dog understands the “quiet” command, you will be able to get their attention and have them follow your new command when their barking goes too far. Again, it is best to start this training as soon as you can since it can be much more difficult to “un-train” a watchdog. </p></li>
          </ul> <!-- /section 4: watchdog -->


