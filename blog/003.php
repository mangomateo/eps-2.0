          <!-- intro -->
          <p>There’s nothing pleasant about a dog barking uncontrollably. The truth is that you will likely never be able to completely “un-train” your dog to bark and nor should you. Barking is one of the ways your dog communicates. It is completely natural and can give you a good sense of how your dog is feeling. They want to protect you, tell you what they’re excited about and what they’re nervous about.</p>

          <p>You don’t want to stop your dog from expressing his or herself, but you do want to be able to have control over a situation if they start to take the habit a little too far.</p>

          <p>The first course of action is to try and identify why he or she is barking.  Once you have determined <em>why</em> your dog is barking, you can start working on how to get it to stop.</p>

          <p>Despite the reason, here are a few tips to keep in mind with an overly vocal dog:</p>

          <ul class="py-4">
            <li><p>Your very first step in any situation should be distracting your dog from whatever it’s barking at. Start this early to teach your dog that it is okay to bark, but once we’ve expressed what we’re feeling it’s time to move on to something else. It’s also important to teach your dog to break their attention from what they’re barking at and pay attention to you on command.</p></li>
            <li><p><em><strong>Do not yell at your dog to stop barking!</strong></em> Consider what your pet is thinking when he starts barking and you start yelling: “Look! Mom/Dad is barking too! I must be doing something right and should continue! Look how much fun we’re having together while we protect the house from that delivery man!”</p></li>
          </ul>

          <p>Now that we have a starting point, let’s dig a little deeper. Here are some of the most common reasons a dog may bark excessively and how to get it to stop.</p> <!-- /intro -->


          <!-- section 1: boredom -->
          <h2 class="font-weight-bold py-4">Boredom</h2>

          <p>Dogs can get bored easily. A lot of the time, your pet was bred with a hard-wired job, therefore making domestic life boring for an intelligent animal that is looking for a task to complete. In theory, “boredom barks” are one of the easier types of barks to get rid of. Relieve their boredom!</p>

          <ul class="py-4">
            <li><p>The easiest way to relieve their boredom is by helping them to use more of their energy! Instead of a one, half-hour walk a day, your dog may need 2 or even 3, half-hour walks a day. Instead of just walking, try playing fetch or taking up agility with your pet. It will give your dog the feeling of purpose and pride, build your bond and help to relieve their pent up energy.</p></li>
            <li><p>Have a pet professional come visit your pet throughout the day if you’re out of the house all day. A mid-day visitor and an afternoon walk may break up the day nicely and help them settle during the other times of the day.</p></li>
            <li><p>Give them enriching activities to do when they’re alone. Instead of a typical bone or stuffed animal, offer them something that takes more time to tire of and is more rewarding to them. For example a Kong toy might be perfect. If you fill it with healthy treats the dog is willing to work with the toy longer, knowing that the perseverance means a yummy pay off. If they play with their Kong when you’re not home, they certainly are not barking and are simultaneously getting rewarded for participating in a quiet activity.</p></li>
          </ul> <!-- /section 1: boredom -->


          <!-- section 2: attention -->
          <h2 class="font-weight-bold pb-4">Attention</h2>

          <p>If your dog is barking to get your attention, the best thing that you can do for him is to ignore it!</p>

          <ul class="py-4">
            <li><p>This can be hard to do because as we grow more irritated it becomes harder to ignore, especially when you know that acknowledging your dog will end the noise. Always remember that to your pup, even negative attention (“No” or “Wrong”) is better than no attention. Eventually your dog <em>will</em> realize “Hey, when I bark, no one pays attention to me. This is the opposite of what I want. I will try something different”.</p></li>
            <li><p>Only once your dog has stopped barking and is quiet for 15 seconds should you give the attention that he’s craving with a treat and praise. This will cue the “when I was quiet, <em>then</em> I got attention” realization.</p></li>
          </ul> <!-- /section 2: attention -->


          <!-- section 3: anxiety & fear -->
          <h2 class="font-weight-bold pb-4">Anxiety and Fear</h2>

